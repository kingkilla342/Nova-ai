const API = 'https://api.github.com';

// Store GitHub credentials in localStorage
export function getGitHubCreds() {
  if (typeof window === 'undefined') return null;
  try {
    return JSON.parse(localStorage.getItem('nova_github')) || null;
  } catch { return null; }
}

export function saveGitHubCreds(creds) {
  if (typeof window === 'undefined') return;
  localStorage.setItem('nova_github', JSON.stringify(creds));
}

export function clearGitHubCreds() {
  if (typeof window === 'undefined') return;
  localStorage.removeItem('nova_github');
}

// ═══════════════════════════════════════════
// GITHUB ACTIONS WORKFLOW
// ═══════════════════════════════════════════

function generateWorkflow(javaVersion = 17) {
  return `name: Build Plugin
on:
  push:
    branches: [main]
  workflow_dispatch:

jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Java ${javaVersion}
        uses: actions/setup-java@v4
        with:
          java-version: '${javaVersion}'
          distribution: 'temurin'

      - name: Setup Gradle
        uses: gradle/actions/setup-gradle@v3

      - name: Make gradlew executable
        run: chmod +x gradlew || true

      - name: Build with Gradle
        run: |
          if [ -f "gradlew" ]; then
            ./gradlew build shadowJar --no-daemon
          else
            gradle build shadowJar --no-daemon
          fi

      - name: Upload JAR
        uses: actions/upload-artifact@v4
        with:
          name: plugin-jar
          path: |
            build/libs/*.jar
            !build/libs/*-dev.jar
            !build/libs/*-sources.jar
          retention-days: 30
`;
}

// Minimal Gradle wrapper properties
function generateWrapperProps() {
  return `distributionBase=GRADLE_USER_HOME
distributionPath=wrapper/dists
distributionUrl=https\\://services.gradle.org/distributions/gradle-8.5-bin.zip
networkTimeout=10000
validateDistributionUrl=true
zipStoreBase=GRADLE_USER_HOME
zipStorePath=wrapper/dists
`;
}

// Gradlew shell script
function generateGradlew() {
  return `#!/bin/sh
# Gradle startup script — auto-generated by Nova
APP_BASE_NAME=\`basename "$0"\`
CLASSPATH=$APP_HOME/gradle/wrapper/gradle-wrapper.jar
exec java -classpath "$CLASSPATH" org.gradle.wrapper.GradleWrapperMain "$@"
`;
}

// ═══════════════════════════════════════════
// API HELPERS
// ═══════════════════════════════════════════

async function githubFetch(path, token, options = {}) {
  const res = await fetch(`${API}${path}`, {
    ...options,
    headers: {
      'Authorization': `token ${token}`,
      'Accept': 'application/vnd.github.v3+json',
      'Content-Type': 'application/json',
      ...options.headers,
    },
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`GitHub API ${res.status}: ${text}`);
  }
  return res.json();
}

