export function generateModTemplate(projectName, packageName, mcVersion = '1.20.4') {
  const safeName = projectName.replace(/[^a-zA-Z0-9]/g, '');
  const modId = safeName.toLowerCase();
  const pkg = packageName || `com.example.${modId}`;
  const pkgPath = pkg.replace(/\./g, '/');
  const ver = MC_VERSIONS.find(v => v.value === mcVersion) || MC_VERSIONS[4];

  return {
    [`src/main/java/${pkgPath}/${safeName}Mod.java`]: `package ${pkg};

import net.minecraftforge.fml.common.Mod;
import net.minecraftforge.fml.event.lifecycle.FMLCommonSetupEvent;
import net.minecraftforge.fml.javafmlmod.FMLJavaModLoadingContext;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;

@Mod("${modId}")
public class ${safeName}Mod {

    private static final Logger LOGGER = LogManager.getLogger();

    public ${safeName}Mod() {
        FMLJavaModLoadingContext.get().getModEventBus().addListener(this::setup);
    }

    private void setup(final FMLCommonSetupEvent event) {
        LOGGER.info("${safeName} initialized!");
    }
}`,

    'src/main/resources/META-INF/mods.toml': `modLoader="javafml"
loaderVersion="[47,)"
license="All rights reserved"

[[mods]]
modId="${modId}"
version="1.0.0"
displayName="${projectName}"
description="Created with Nova AI"`,

    'build.gradle': `plugins {
    id 'java'
    id 'net.minecraftforge.gradle' version '6.0.+'
}

version = '1.0.0'
group = '${pkg}'

java.toolchain.languageVersion = JavaLanguageVersion.of(${ver.java})

minecraft {
    mappings channel: 'official', version: '${mcVersion}'
}

repositories {
    mavenCentral()
}

dependencies {
    minecraft 'net.minecraftforge:forge:${mcVersion}-49.0.30'
}`,

    'settings.gradle': `pluginManagement {
    repositories {
        gradlePluginPortal()
        maven { url 'https://maven.minecraftforge.net/' }
    }
}
rootProject.name = '${safeName}'`,
  };
}

