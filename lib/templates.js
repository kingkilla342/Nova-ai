export function generateDatapackTemplate(projectName, _pkg, mcVersion = '1.20.4') {
  const safeName = projectName.replace(/[^a-zA-Z0-9_]/g, '').toLowerCase();
  const ver = MC_VERSIONS.find(v => v.value === mcVersion) || MC_VERSIONS[4];

  return {
    'pack.mcmeta': JSON.stringify({
      pack: { pack_format: ver.packFormat, description: `${projectName} - Created with Nova AI` },
    }, null, 2),
    [`data/${safeName}/functions/main.mcfunction`]: `# ${projectName} - Main Function\n# Minecraft ${mcVersion}\n# Generated by Nova AI\n\nsay ${projectName} loaded!`,
    [`data/${safeName}/functions/tick.mcfunction`]: `# Runs every tick\n# Add tick logic here`,
    [`data/minecraft/tags/functions/load.json`]: JSON.stringify({ values: [`${safeName}:main`] }, null, 2),
    [`data/minecraft/tags/functions/tick.json`]: JSON.stringify({ values: [`${safeName}:tick`] }, null, 2),
  };
}

